// Route for refining a text document
router.post('/refine', async (req, res) => {
  try {
    const { previousText, instruction, lang, tone } = req.body;
    if (!previousText || !instruction) {
      return res.status(400).json({ error: 'Previous text and instruction are required' });
    }

    const API_KEY = process.env.GEMINI_API_KEY;
    if (!API_KEY) {
      console.warn('⚠️ GEMINI_API_KEY is not configured. Running in demo mode for refinement.');
      // Demo refinement: append instruction or mock edit
      const prefix = lang === 'am' ? '\n\n[የተስተካከለ እትም በ AI]፦\n' : '\n\n[AI Refinement Update]:\n';
      const instructionText = lang === 'am' ? `ማስተካከያ የተደረገበት: ${instruction}` : `Adjusted according to: "${instruction}"`;
      return res.json({ reply: previousText + prefix + instructionText });
    }

    const userPrompt = `
      Original Document:
      """
      ${previousText}
      """

      Refinement Instruction:
      "${instruction}"

      Desired Language: ${lang === 'am' ? 'Amharic' : 'English'}
      Desired Tone: ${tone || 'professional'}
    `;

    const SYSTEM_REFINE_PROMPT = `You are the AI Writing Assistant for "Bole Animal Feed Processing PLC".
    You are given a previously generated document and a user's instruction to refine, edit, format, or translate it.
    Apply the requested adjustment precisely while keeping the format clean and professional.
    Do NOT include any greetings or commentary. Output ONLY the updated document.`;

    const response = await fetch(`https://generativelanguage.googleapis.com/v1beta/models/gemini-1.5-flash:generateContent?key=${API_KEY}`, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({
        contents: [{ role: 'user', parts: [{ text: userPrompt }] }],
        systemInstruction: {
          parts: [{ text: SYSTEM_REFINE_PROMPT }]
        },
        generationConfig: {
          temperature: 0.5,
          maxOutputTokens: 1000
        }
      })
    });

    const data = await response.json();
    
    if (!response.ok) {
      console.error('Gemini API Error:', data);
      return res.status(502).json({ error: 'Error connecting to AI service' });
    }

    const reply = data.candidates?.[0]?.content?.parts?.[0]?.text || previousText;
    res.json({ reply });
  } catch (error) {
    console.error('AI refine endpoint error:', error);
    res.status(500).json({ error: 'Internal server error' });
  }
});

